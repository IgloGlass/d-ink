#!/usr/bin/env node
import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";

function fail(message) {
  throw new Error(message);
}

async function readTicketPayloadV1() {
  const payloadPath = process.env.AUTOPILOT_TICKET_PAYLOAD_PATH;
  if (!payloadPath) {
    fail("AUTOPILOT_TICKET_PAYLOAD_PATH is required.");
  }

  const rawPayload = await readFile(path.resolve(payloadPath), "utf8");
  return JSON.parse(rawPayload);
}

async function writeNight001V1(payload) {
  const targetPath = path.join(
    process.cwd(),
    "docs",
    "autopilot-demo",
    "night-001-kickoff.md",
  );
  await mkdir(path.dirname(targetPath), { recursive: true });
  const body = [
    "# Night Autopilot Kickoff",
    "",
    `- run_id: ${payload.run_id}`,
    `- ticket_id: ${payload.ticket.ticket_id}`,
    `- branch_name: ${payload.branch_name}`,
    `- timestamp: ${new Date().toISOString()}`,
    "",
    "This artifact is generated by the deterministic autopilot executor.",
    "",
  ].join("\n");
  await writeFile(targetPath, body, "utf8");
  return {
    written_file: "docs/autopilot-demo/night-001-kickoff.md",
  };
}

async function writeNight002V1(payload) {
  const targetPath = path.join(
    process.cwd(),
    "docs",
    "autopilot-demo",
    "night-002-follow-up.json",
  );
  await mkdir(path.dirname(targetPath), { recursive: true });
  const body = {
    run_id: payload.run_id,
    ticket_id: payload.ticket.ticket_id,
    branch_name: payload.branch_name,
    timestamp: new Date().toISOString(),
    source: "scripts/autopilot-ticket-executor.mjs",
  };
  await writeFile(`${targetPath}`, `${JSON.stringify(body, null, 2)}\n`, "utf8");
  return {
    written_file: "docs/autopilot-demo/night-002-follow-up.json",
  };
}

async function main() {
  const payload = await readTicketPayloadV1();
  const ticketId = payload?.ticket?.ticket_id;
  if (!ticketId) {
    fail("Ticket payload is missing ticket.ticket_id.");
  }

  let result;
  if (ticketId === "NIGHT-001") {
    result = await writeNight001V1(payload);
  } else if (ticketId === "NIGHT-002") {
    result = await writeNight002V1(payload);
  } else {
    fail(`No deterministic handler configured for ticket_id: ${ticketId}`);
  }

  console.log(
    JSON.stringify(
      {
        ok: true,
        ticket_id: ticketId,
        ...result,
      },
      null,
      2,
    ),
  );
}

main().catch((error) => {
  const message = error instanceof Error ? error.message : "Unknown error";
  console.error(`Ticket executor failed: ${message}`);
  process.exitCode = 1;
});
